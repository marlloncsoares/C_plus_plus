#include <iostream>
using namespace std;

// Algoritmo realiza a soma de dois números inteiros.

int main() {
    // Declara as variáveis que serão utilizadas.
    int a, b, s;
    // Realiza a leitura dos dados.
    cin >> a >> b;
    // Faz o processamento dos dados.
    s = a + b;
    // Mostra o resultado para o usuário.
    cout << "X = " << s << endl;
    return 0;
}

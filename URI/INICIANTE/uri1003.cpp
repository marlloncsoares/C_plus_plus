#include <iostream>
 
using namespace std;

// Algoritmo realiza a soma entre dois números inteiros.
int main() {
    // Declara as variáveis que serão utilizadas.
    int a, b, soma;
    // Realiza a leitura dos dados.
    cin >> a >> b;
    // Realiza o processamento dos dados.
    soma = a + b;    
    // Mostra o resultado final.
    cout << "SOMA = " << soma << endl; 
    return 0;

}
#include <iostream>

using namespace std;

// Algoritmo para calcular o produto entre dois números inteiros.
 
int main() {
    
    // Declara as variáveis que serão utilizadas
    int x, y, p;
    // Faz a leitura dos dados.
    cin >> x >> y;
    // Realiza o processamento.
    p = x * y;
    // Mostra o resultado.
    cout << "PROD = " << p << endl;
    return 0;
}